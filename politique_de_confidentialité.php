    <p>&nbsp;Cookies</p>
    <p>&nbsp;</p>
    <p>Lors de la consultation du Site, des informations relatives à votre navigation sont susceptibles d'être enregistrées dans des fichiers &quot;Cookies&quot; installés sur votre terminal (ordinateur, tablette, smartphone…), sous réserve des choix exprimés concernant les cookies et que vous pouvez modifier à tout moment.</p>
    <p>&nbsp;</p>
    <p>Les informations ci-dessous vous permettent de mieux comprendre le fonctionnement des cookies et l'utilisation des outils actuels afin de les paramétrer.</p>
    <p>&nbsp;</p>
    <p>Qu’est-ce qu’un cookie ?</p>
    <p>&nbsp;</p>
    <p>Un cookie est un petit fichier texte, image ou logiciel, contenant des informations, qui est enregistré sur le disque dur de votre terminal (ex : ordinateur, tablette, smartphone, ou tout appareil permettant de naviguer sur Internet) à l'occasion de la consultation d'un site Internet avec votre logiciel de navigation. Il est transmis par le serveur d'un site Internet à votre navigateur. Le fichier cookie porte notamment sur les pages visitées, les publicités sur lesquelles vous avez cliquées, le type de navigateur que vous utilisez, votre adresse IP, et permet à son émetteur d'identifier le terminal dans lequel il est enregistré, pendant la durée de validité ou d'enregistrement du cookie concerné.</p>
    <p>&nbsp;</p>
    <p>Seul l'émetteur d'un cookie est susceptible de lire ou de modifier des informations qui y sont contenues.</p>
    <p>&nbsp;</p>
    <p>Les cookies améliorent votre confort de navigation sur notre Site et sont indispensables pour accéder à certains espaces sécurisés. Si vous décidez de bloquer tous les cookies par l’intermédiaire de votre navigateur internet, vous ne pourrez plus visiter que la partie publique de notre Site.</p>
    <p>&nbsp;</p>
    <p>Comme indiqué sur le bandeau d’information du Site, en poursuivant votre navigation sur le Site, vous acceptez le dépôt de cookies sur votre terminal. Vous pouvez revenir sur cette décision à tout moment en vous opposant au dépôt des cookies (voir ci-dessous : s’opposer au dépôt des cookies).</p>
    <p>&nbsp;</p>
    <p>Pourquoi l’utilisation des cookies sur le Site ?</p>
    <p>&nbsp;</p>
    <p>Le Site utilise 3 catégories de cookies dont les finalités sont décrites ci-après.</p>
    <p>&nbsp;</p>
    <p>Certains cookies techniques (cookies de session notamment) sont indispensables pour le bon fonctionnement du Site. Leur suppression peut entrainer des difficultés de navigation ainsi que l'impossibilité de souscrire en ligne à nos offres.</p>
    <p>&nbsp;</p>
    <p>&nbsp;1. Les cookies techniques</p>
    <p>&nbsp;</p>
    <p>Les cookies techniques sont des cookies strictement nécessaires au fonctionnement du Site. Ils permettent d'accéder aux espaces réservés et personnels du Site, grâce aux identifiants personnels (cookie identifiant de session), de mettre en œuvre des mesures de sécurité (par exemple, lorsqu'il est demandé de se connecter à nouveau à un contenu ou à un service après un certain laps de temps), de permettre ou de faciliter la communication pour la fourniture d'un service par voie électronique expressément demandé par l'internaute. Ces cookies sont supprimés dès que vous fermez votre session de navigation.</p>
    <p>&nbsp;</p>
    <p>Ces cookies sont émis et déposés par locadem. Ils ont une durée de vie très courte, pour la majorité, le temps de la session, et au maximum d'une année.</p>
    <p>&nbsp;</p>
    <p>En l’occurrence locadem utilise un seul cookie de ce type. Il s’agit du cookie de session. Sa durée de vie est égale au temps de la session de l’Utilisateur. Ce temps est limité à 1 heure en cas d’inactivité et au maximum, à 24 heures.</p>
    <p>&nbsp;</p>
    <p>&nbsp;2. Les cookies de mesure d’audience (réalisation de statistiques)</p>
    <p>&nbsp;</p>
    <p>Ces cookies recueillent des informations sur la façon dont les visiteurs utilisent un site internet. Par exemple, le nombre de vis, les pages les plus visitées… Ces cookies permettant d’établir des statistiques d’analyse de la fréquentation à partir desquels les contenus du Site sont améliorés en fonction du succès rencontré par telle ou telle page auprès de nos visiteurs.</p>
    <p>&nbsp;</p>
    <p>XITI, l'outil de statistiques utilisé par locadem, génère un cookie avec un identifiant unique, dont la durée est limitée à 6 mois. Nous collectons l'adresse IP, afin d'identifier la ville depuis laquelle vous vous connectez. Les deux derniers octets sont immédiatement anonymisés. locadem ne peut donc en aucun cas identifier un individu internaute via les données collectées.</p>
    <p>&nbsp;</p>
    <p>Les données statistiques obtenues ne sont pas cédées à des tiers ni utilisées à d’autres fins.</p>
    <p>&nbsp;</p>
    <p>&nbsp;3. Les cookies de partage (réseaux sociaux)</p>
    <p>&nbsp;</p>
    <p>Le Site contient des liens de partage vers Facebook, Twitter et autres réseaux sociaux similaires, qui vous permettent de partager des contenus du Site avec d’autres personnes. Lorsque vous utilisez ces boutons de partage, votre navigateur internet va établir une connexion avec les serveurs des réseaux sociaux concernés et un cookie tiers est installé après recueil de votre consentement. Si vous êtes connectés au réseau social lors de votre navigation sur le Site, les boutons de partage permettent de relier les contenus consultés à votre compte utilisateur.</p>
    <p>&nbsp;</p>
    <p>Dans ce contexte, les finalités et la portée de l’utilisation de vos données, les traitements supplémentaires ainsi que vos droits seront soumis aux politiques de confidentialité des fournisseurs de réseaux sociaux et placés sous leur entière et unique responsabilité.</p>
    <p>&nbsp;</p>
    <p>&nbsp;4. Les cookies ciblés ou publicitaires</p>
    <p>&nbsp;</p>
    <p>Ce type de cookies ne peut être installé qu’avec votre consentement préalable.</p>
    <p>&nbsp;</p>
    <p>Ces cookies collectent des informations concernant la navigation de votre terminal sur le Site afin de vous proposer des publicités ciblées qui vous sont adaptées et/ou correspondant à vos centres d’intérêt. Ils nous permettent d’adapter les contenus publicitaires affichés sur votre terminal dans nos espaces publicitaires, suivant les informations de navigation recueillies.</p>
    <p>&nbsp;</p>
    <p>Nous sommes également susceptibles, sous réserve de vos choix, d’associer des informations de navigation relatives à votre terminal recueillies par les cookies que nous émettons, avec vos données personnelles que vous nous avez transmises lors de votre accès à votre espace personnel, afin de vous adresser par exemple des propositions/offres commerciales électroniques ou d’afficher sur nos espaces publicitaires ou sur les sites de nos partenaires (sites tiers) des publicités personnalisées qui vous sont spécifiquement destinées et susceptibles de vous intéresser personnellement.</p>
    <p>&nbsp;</p>
    <p>Ces cookies nous permettent également de limiter le nombre de parutions d’une publicité et nous aident à évaluer l’efficacité de nos campagnes publicitaires.</p>
    <p>&nbsp;</p>
